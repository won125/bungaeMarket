COMMENT ON COLUMN GOODS.STATUS IS '상품 상태';

COMMENT ON COLUMN GOODS.GOODSINFO IS '상품 설명';

COMMENT ON COLUMN GOODS.GOODSCOUNT IS '상품 수량';

COMMENT ON COLUMN GOODS.LCATEGORY IS '대분류 카테고리';

COMMENT ON COLUMN GOODS.SCATEGORY IS '소분류 카테고리';

COMMENT ON COLUMN GOODS.TAG IS '연관태그';

