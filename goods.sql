CREATE TABLE GOODS 
(
  GOODSCODE VARCHAR2(200 BYTE) NOT NULL 
, GOODSTITLE VARCHAR2(100 BYTE) NOT NULL 
, GOODSIMAGE VARCHAR2(500 BYTE) NOT NULL 
, GOODSPRICE NUMBER NOT NULL 
, CHANGE VARCHAR2(100 BYTE) NOT NULL 
, GOODSLOCATION VARCHAR2(200 BYTE) NOT NULL 
, STATUS VARCHAR2(200 BYTE) NOT NULL 
, GOODSINFO VARCHAR2(2000 BYTE) NOT NULL 
, GOODSCOUNT NUMBER NOT NULL 
, LCATEGORY VARCHAR2(100 BYTE) NOT NULL 
, SCATEGORY VARCHAR2(100 BYTE) NOT NULL 
, TAG VARCHAR2(200 BYTE) 
, SAFEPAY VARCHAR2(100 BYTE) 
, DELIVERYPRICE VARCHAR2(200 BYTE) 
, STORENAME VARCHAR2(100 BYTE) NOT NULL 
, T_STATUS VARCHAR2(100 BYTE) 
, CONSTRAINT GOODS_PK PRIMARY KEY 
  (
    GOODSCODE 
  )
  USING INDEX 
  (
      CREATE UNIQUE INDEX GOODS_PK ON GOODS (GOODSCODE ASC) 
      LOGGING 
      TABLESPACE USERS 
      PCTFREE 10 
      INITRANS 2 
      STORAGE 
      ( 
        BUFFER_POOL DEFAULT 
      ) 
      NOPARALLEL 
  )
  ENABLE 
) 
LOGGING 
TABLESPACE USERS 
PCTFREE 10 
INITRANS 1 
STORAGE 
( 
  BUFFER_POOL DEFAULT 
) 
NOCOMPRESS 
NO INMEMORY 
NOPARALLEL;

COMMENT ON COLUMN GOODS.GOODSCODE IS '상품 코드';

